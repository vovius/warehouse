package com.dio.javamentoring.warehouse;

class Action {
	private String name;
	private String method;
	
	public Action(String name, String method) {
		this.name = name;
		this.method = method;
	}
	
	public String getName() {
		return name;
	}
	public String getMethod() {
		return method;
	}

	
	
}
